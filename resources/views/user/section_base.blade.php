            <p class="d-inline" style="font-size:1.1em">Manifestaciones Clínicas</p>&nbsp;&nbsp;
            <a href=""><i class="far fa-edit" style="font-size:.75em"></i></a>

            <ul style="margin-left: -15px" class="mt-2">
              <li>La huntingtina es una proteina citoplasmatica muy bien conservada que se distribuye de forma amplia en las neuronas de todo el SNC, pero se desconoce su funcion.</li>
              <li class="mt-1">En las generaciones posteriores se muestra un numero todavia mayor de repeticiones y la enfermedad comenzara en etapas mas precoces, a este fenomeno se le conoce como anticipación.</li>
              <li class="mt-1">Cuando hay de 36 a 39 repeticiones se observa una afectación clínica menos grave </li>
              <li class="mt-1">Los cambios cognitivos en la AD tienden a seguir un patrón característico que comienza con alteración de la memoria y evoluciona a deficiencias del lenguaje y visuoespaciales.</li>
              <li class="mt-1">cerca de 20% de los pacientes con AD se presentan con síntomas no relacionados con la memoria, como dificultad para encontrar las palabras, de organización y de navegación.</li>
              <li class="mt-1">En otros pacientes, las principales manifestaciones de la AD son disfunción del procesamiento visual ascendente (referido como síndrome de atrofi a cortical posterior) o una afasia “logopénica” progresiva durante años antes que haya afectación de la memoria y de otros dominios cognitivos</li>
            </ul>

            <p class="d-inline font-italic" style="font-size:1.05em">Etapas Tempranas</p>&nbsp;&nbsp;
            <a href=""><i class="far fa-edit" style="font-size:.75em"></i></a>
            <ul style="margin-left: -15px" class="mt-2">
              <li>La amnésica típica y la pérdida de la memoria pueden pasar inadvertidamente o adjudicarse a olvidos benignos del envejecimiento.</li>
              <li class="mt-1">Una vez que la pérdida de la memoria se vuelve notoria para el paciente y el cónyuge, y desciende 1.5 desviaciones estándar por debajo de las pruebas de memoria estandarizadas, se aplica el término daño cognitivo leve (MCI, mild cognitive impairment).</li>             
            </ul>

            <p class="d-inline font-italic" style="font-size:1.05em">Etapas Tempranas</p>&nbsp;&nbsp;
            <a href=""><i class="far fa-edit" style="font-size:.75em"></i></a>
            <ul style="margin-left: -15px" class="mt-2">
              <li>Al final, con la AD los problemas cognitivos empiezan a interferir en las actividades diarias, como manejar las fi nanzas personales, seguir instrucciones en el trabajo, conducir un vehículo, hacer compras y realizar las labores domésticas..</li>
              <li class="mt-1">Algunos pacientes no están conscientes de estas difi cultades (anosognosia), pero la mayoría perciben sus defi ciencias. Los cambios en el ambiente (viajes, cambios de domicilio, hospitalización) tienden a desestabilizar al paciente.</li>
             
            </ul>
            <p class="d-inline" style="font-size:1.1em">Consideraciones Clinicas</p>&nbsp;&nbsp;
            <a href=""><i class="far fa-edit" style="font-size:.75em"></i></a>

            <ul style="margin-left: -15px" class="mt-2">
              <li>Varios genes tienen una función importante en la patogenia de la AD. Uno es el gen APP en el cromosoma 21.</li>
              <li class="mt-1">ELos adultos con trisomía 21 (síndrome de Down) siempre desarrollan los rasgos neuropatológicos típicos de AD si sobreviven >40 años, y muchos padecen una demencia progresiva que se superpone a su retraso mental basal</li>
              <li class="mt-1">La dosis adicional del gen APP en el cromosoma 21 es la causa original de la AD en el adulto con síndrome de Down y conduce a la producción excesiva de amiloide cerebral. </li>
            </ul>

